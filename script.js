var question1=document.getElementById("que1");
var dropdown1=document.getElementById("more1");
var quel1=document.getElementById("quel1");


dropdown1.onclick = function(){

    if(question1.className == "open"){
        
        question1.className="";
        quel1.className="";


    }else{

        question1.className = "open";
        quel1.className="bold";
    }
};
